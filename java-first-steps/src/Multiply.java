
public class Multiply {

	public static void main(String[] args) {
		
		for (int i = 0; i <= 10; i++) {
			
			for (int j = 0; j <= 10; j++) {
				int resulset = i*j;
				System.out.print(i + " * " + j + " = " + resulset+"; ");
				System.out.print("");				
				
			}
			System.out.println();
		}

	}

}
